}
local function armorTypeToken(types)
    -- call as armorTypeToken{PLATE={12345}}
    local ret = {}
    for armorType, loot in pairs(types) do
        for _, class in ipairs(armorTypes[armorType]) do
            ret[class] = loot
        end
    end
    return ret
end

