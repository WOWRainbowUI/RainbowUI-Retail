-- ============================================================================
-- MSUF - runtime/local UI string coverage
-- Adds translations for strings used outside the Menu2 page audit.
-- Existing locale entries are preserved.
-- ============================================================================
local addonName, ns = ...
ns = ns or _G.MSUF_NS or {}

local function AddMissing(locale, dict)
    if not locale or type(dict) ~= "table" or not ns.RegisterLocale then return end
    local L = ns.RegisterLocale(locale)
    if not L then return end
    for key, value in pairs(dict) do
        if type(key) == "string" and type(value) == "string" then
            local current = L[key]
            if current == nil or current == key then
                L[key] = value
            end
        end
    end
end

